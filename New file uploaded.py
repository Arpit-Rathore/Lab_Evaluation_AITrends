'''Python program to convert Fahrenheit temperature into Centigrade degrees.'''

f_temp=float(input("Enter temperature of the city in Fahrenheit degrees: "))
c_temp=(5*(f_temp-32))/9
print("Temperature of the city in Centigrade degrees: ",c_temp)


#Output
'''
Enter temperature of the city in Fahrenheit degrees: 98
Temperature of the city in Centigrade degrees:  36.666666666666664
'''
